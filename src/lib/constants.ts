/**
 * 鼠标位置轮询间隔（毫秒）
 *
 * **用途**: 用于检测鼠标是否在角色交互区域内
 *
 * **使用位置**: animation/+page.svelte 中的 handleMouseMove 函数
 *
 * **取值说明**:
 * - 200ms = 每 0.2 秒检测一次鼠标位置
 * - 较短的间隔可提供更及时的交互响应
 * - 较长的间隔可减少 CPU 占用
 *
 * **调整建议**: 如果感觉交互延迟，可以降低到 150ms；如果 CPU 占用过高，可以提高到 300ms
 */
export const CURSOR_POLL_INTERVAL_MS = 200;

// ========================================================================= //
// 打字机效果常量
// ========================================================================= //

/**
 * 打字机效果默认速度（毫秒/字符）
 *
 * **用途**: 每个字符显示的间隔时间，控制对话气泡文本逐字显示的速度
 *
 * **使用位置**: TypewriterText.svelte 组件中的 speed prop 默认值
 *
 * **取值说明**:
 * - 50ms = 每 0.05 秒显示一个字符（约 20 字符/秒）
 * - 较短的值会让文本显示更快（如 30ms = 33 字符/秒）
 * - 较长的值会让文本显示更慢（如 80ms = 12.5 字符/秒）
 *
 * **用户体验建议**:
 * - 中文文本可稍快（40-60ms），因为汉字阅读速度较快
 * - 英文长段落可稍慢（50-70ms），便于阅读理解
 *
 * **注意**: 可以通过 state.text 中的 typeSpeed 字段覆盖此默认值
 */
export const TYPEWRITER_DEFAULT_SPEED_MS = 50;

// ========================================================================= //
// 气泡常量
// ========================================================================= //

/**
 * 气泡切换延迟（毫秒）
 *
 * **用途**: 当新气泡出现时，先隐藏旧气泡的延迟时间，避免两个气泡同时显示
 *
 * **使用位置**: BubbleManager.svelte 中的 show 方法
 *
 * **取值说明**:
 * - 250ms = 旧气泡开始消失动画后，等待 0.25 秒再显示新气泡
 * - 需要配合 BUBBLE_CLOSE_ANIMATION_MS 使用，确保旧气泡完全消失后再显示新气泡
 *
 * **计算公式**: 理想延迟 >= 关闭动画时长 + 一小段缓冲时间
 * - 当前设置: 250ms (200ms 动画 + 50ms 缓冲)
 *
 * **调整建议**: 如果切换过快导致视觉突兀，可增加到 300-400ms
 */
export const BUBBLE_SWITCH_DELAY_MS = 250;

/**
 * 气泡默认自动关闭时间（毫秒）
 *
 * **用途**: 当对话气泡没有分支选项时，文本显示完成后自动关闭的时间
 *
 * **使用位置**: Bubble.svelte 组件中的 startAutoClose 函数
 *
 * **取值说明**:
 * - 2000ms = 文本完全显示后，等待 2 秒自动关闭
 * - 从文本显示完成开始计时，不是从气泡出现开始计时
 *
 * **计算方式**:
 * - 完整显示时间 = (文本字符数 × 打字速度) + 自动关闭延迟
 * - 示例: 10 字符 × 50ms + 2000ms = 2500ms 总时长
 *
 * **调整建议**:
 * - 短文本可缩短到 1500ms，避免用户等待
 * - 长对话可延长到 3000ms，给予用户充分阅读时间
 * - 有分支选项时不会使用此自动关闭，需用户点击选择
 *
 * **注意**: 可以通过 state.text 中的 duration 字段覆盖此默认值
 */
export const BUBBLE_DEFAULT_AUTO_CLOSE_MS = 2000;

/**
 * 气泡关闭动画时长（毫秒）
 *
 * **用途**: 气泡消失时的淡出动画时长
 *
 * **使用位置**: Bubble.svelte 组件中的 close 方法
 *
 * **取值说明**:
 * - 200ms = 0.2 秒完成淡出动画
 * - 使用 CSS transition/animation 实现，从 opacity: 1 渐变到 0
 *
 * **动画效果**:
 * - 配合 scale 变换，实现缩小+淡出的组合效果
 * - CSS spring 或 cubic-bezier 可增加弹性感
 *
 * **调整建议**:
 * - 过快（<100ms）可能让用户来不及注意到气泡关闭
 * - 过慢（>400ms）可能让用户感觉响应迟钝
 * - 200-300ms 是平衡视觉效果和响应性的理想范围
 */
export const BUBBLE_CLOSE_ANIMATION_MS = 200;

/**
 * 托盘吸附时的垂直偏置（像素）
 *
 * **用途**: 当窗口吸附到系统托盘区域时，气泡与托盘图标的垂直间距
 *
 * **使用位置**: animation/+page.svelte 中的 calculateAdaptivePosition 函数
 *
 * **取值说明**:
 * - 20px = 气泡下边缘比托盘图标高 20 像素
 * - 避免气泡遮挡托盘图标，同时保持视觉关联
 *
 * **适用场景**:
 * - 窗口吸附到屏幕顶部或底部时使用
 * - 自由浮动窗口模式下不使用
 *
 * **调整建议**:
 * - 根据实际角色图标的头部高度调整
 * - 大图标可能需要更大偏置（30-40px）
 * - 小图标可减少偏置（10-15px）
 */
export const TRAY_ADAPTIVE_OFFSET_Y = 20;

