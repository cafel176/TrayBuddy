/**
 * 调试面板时钟更新间隔（毫秒）
 *
 * **用途**: EnvironmentDebugger 组件中本地时钟的更新频率
 *
 * **使用位置**: EnvironmentDebugger.svelte 中的 updateTime setInterval
 *
 * **取值说明**:
 * - 3000ms = 每 3 秒更新一次本地时间显示
 * - 不需要太高精度，因为秒数变化本身就有间隔
 *
 * **与其他时间间隔对比**:
 * - CURSOR_POLL_INTERVAL_MS: 200ms (高频，需要实时响应)
 * - DEBUG_CLOCK_UPDATE_INTERVAL_MS: 3000ms (低频，秒级变化)
 *
 * **调整建议**:
 * - 保持 1000ms 以上，因为时钟秒数变化就是 1 秒
 * - 过高（>5000ms）会让时钟显示明显滞后
 * - 3000ms 提供了合理的刷新频率和性能平衡
 */
export const DEBUG_CLOCK_UPDATE_INTERVAL_MS = 3000;

// ========================================================================= //
// 日志工具
// ========================================================================= //

/**
 * 是否为开发环境
 *
 * **用途**: 区分开发/生产环境，用于条件输出调试信息
 *
 * **使用位置**: 所有需要条件日志的组件
 *
 * **取值说明**:
 * - 由 Vite 的 import.meta.env.DEV 自动设置
 * - 开发模式运行 `npm run dev` 时为 true
 * - 构建生产版本 `npm run build` 后为 false
 *
 * **使用场景**:
 * - 避免生产环境输出敏感信息
 * - 减少生产环境的 console 性能开销
 * - 在组件中用 `IS_DEV && console.log()` 替代纯 console.log
 */
export const IS_DEV = import.meta.env.DEV;

/**
 * 条件日志工具
 *
 * **用途**: 提供带有开发环境检查的日志函数，生产环境自动静默
 *
 * **使用示例**:
 * ```typescript
 * logger.debug("调试信息");     // 仅开发环境输出
 * logger.info("普通信息");      // 仅开发环境输出
 * logger.warn("警告信息");      // 所有环境输出
 * logger.error("错误信息");     // 所有环境输出
 * ```
 *
 * **方法说明**:
 * - debug: 仅开发环境输出，用于临时调试信息
 * - info: 仅开发环境输出，用于状态变更等常规信息
 * - warn: 所有环境输出，用于潜在问题提示
 * - error: 所有环境输出，用于错误和异常
 *
 * **最佳实践**:
 * - 使用 logger.debug 替代 console.log，避免生产环境遗漏删除
 * - 严重错误使用 logger.error，确保生产环境也能看到
 * - 性能敏感的日志使用 logger.debug，生产环境禁用以提升性能
 */
export const logger = {
  debug: (...args: unknown[]) => IS_DEV && console.log(...args),
  info: (...args: unknown[]) => IS_DEV && console.info(...args),
  warn: (...args: unknown[]) => console.warn(...args),
  error: (...args: unknown[]) => console.error(...args),
};
